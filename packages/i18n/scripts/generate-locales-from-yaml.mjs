#!/usr/bin/env node
/**
 * Emits {namespace}.generated.ts next to each YAML (with .json fallback when YAML is empty)
 * for central locales under packages/i18n, and for each app under packages/app-store
 * (apps ... / translations / {locale}).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { parse as parseYaml } from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const LOCALES_DIR = path.join(__dirname, "../src/locales");
const APPS_DIR = path.join(__dirname, "../../app-store/src/apps");

function isEmptyObject(obj) {
  return (
    obj == null ||
    (typeof obj === "object" &&
      !Array.isArray(obj) &&
      Object.keys(obj).length === 0)
  );
}

/**
 * @param {string} localeDir — directory that contains e.g. admin.yaml / admin.json
 * @param {string} stem — namespace (admin, public, …)
 */
function loadSourceObject(localeDir, stem) {
  const yamlPath = path.join(localeDir, `${stem}.yaml`);
  if (fs.existsSync(yamlPath)) {
    const raw = fs.readFileSync(yamlPath, "utf8");
    let doc;
    if (raw.trim()) {
      try {
        doc = parseYaml(raw);
      } catch (e) {
        throw new Error(`Invalid YAML: ${yamlPath}: ${e.message}`);
      }
    } else {
      doc = null;
    }
    if (
      doc != null &&
      typeof doc === "object" &&
      !Array.isArray(doc) &&
      !isEmptyObject(doc)
    ) {
      return doc;
    }
  }
  const jsonPath = path.join(localeDir, `${stem}.json`);
  if (fs.existsSync(jsonPath)) {
    return JSON.parse(fs.readFileSync(jsonPath, "utf8"));
  }
  return {};
}

function getStemsInLocaleDir(localeDir) {
  if (!fs.existsSync(localeDir)) return [];
  const stems = new Set();
  for (const f of fs.readdirSync(localeDir)) {
    if (f.endsWith(".generated.ts") || f.startsWith(".")) continue;
    const m = f.match(/^(.*)\.(json|yaml|yml)$/);
    if (!m) continue;
    stems.add(m[1]);
  }
  return [...stems].sort();
}

function getStemsForLocaleBase(baseLocalesRoot, baseLocale) {
  return getStemsInLocaleDir(path.join(baseLocalesRoot, baseLocale));
}

function getSubdirs(root) {
  if (!fs.existsSync(root)) return [];
  return fs
    .readdirSync(root, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name);
}

function getAppNames() {
  return getSubdirs(APPS_DIR).sort();
}

function objectToTypeScriptValue(value, indent) {
  const pad = " ".repeat(indent);
  if (value === null) return "null";
  if (typeof value === "string") {
    return JSON.stringify(value);
  }
  if (typeof value === "number" || typeof value === "boolean") {
    return String(value);
  }
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    const lines = value.map(
      (v) => `${pad}  ${objectToTypeScriptValue(v, indent + 2)}`,
    );
    return `[\n${lines.join(",\n")}\n${pad}]`;
  }
  if (typeof value === "object") {
    const keys = Object.keys(value);
    if (keys.length === 0) return "{}";
    const lines = keys.map(
      (k) =>
        `${pad}  ${JSON.stringify(k)}: ${objectToTypeScriptValue(value[k], indent + 2)}`,
    );
    return `{\n${lines.join(",\n")}\n${pad}}`;
  }
  return "undefined";
}

const header = `// Generated by packages/i18n/scripts/generate-locales-from-yaml.mjs — do not edit.
// Source: .yaml (with .json fallback while migrating). Re-run: yarn generate-locales
`;

const loaderHeader = `// Generated by packages/i18n/scripts/generate-locales-from-yaml.mjs — do not edit.
// Static import paths (no interpolated locale) so bundlers can include message chunks. Re-run: yarn generate-locales
`;

function writeGeneratedFile(outPath, data) {
  const body = `${header}const messages = ${objectToTypeScriptValue(data, 0)} as const;
export default messages;
`;
  fs.writeFileSync(outPath, body, "utf8");
}

/**
 * @param {string} localeDir
 * @param {string[]} stems
 */
function writeNamespaceLoadersFile(localeDir, stems) {
  const entries = stems
    .map((stem) => `  ${stem}: () => import("./${stem}.generated")`)
    .join(",\n");
  const body = `${loaderHeader}export const namespaceLoaders = {
${entries},
} as const;
`;
  fs.writeFileSync(
    path.join(localeDir, "namespace-loaders.generated.ts"),
    body,
    "utf8",
  );
}

/**
 * @param {string} transRoot — parent of en/, uk/, …
 * @param {string[]} localeCodes
 */
function writeLocaleNamespaceLoadersRegistry(transRoot, localeCodes) {
  const sorted = [...localeCodes].sort();
  const isSafeIdent = (s) => /^[a-zA-Z_$][\w$]*$/.test(s);
  const loaderEntries = sorted
    .map((loc) => {
      const key = isSafeIdent(loc) ? loc : JSON.stringify(loc);
      return `  ${key}: () => import("./${loc}/namespace-loaders.generated")`;
    })
    .join(",\n");
  const body = `${loaderHeader}export type MessageLocale = keyof typeof localeNamespaceLoaders;

export const localeNamespaceLoaders = {
${loaderEntries},
} as const;

export function resolveMessageLocale(locale: string): MessageLocale {
  if (locale in localeNamespaceLoaders) {
    return locale as MessageLocale;
  }
  return "en";
}
`;
  fs.writeFileSync(
    path.join(transRoot, "locale-namespace-loaders.generated.ts"),
    body,
    "utf8",
  );
}

/**
 * @param {string} baseRoot
 * @param {string[]} stems
 * @param {string[]} localeCodes
 */
function emitLoaderModules(baseRoot, stems, localeCodes) {
  if (stems.length === 0 || localeCodes.length === 0) {
    return;
  }
  for (const loc of localeCodes) {
    writeNamespaceLoadersFile(path.join(baseRoot, loc), stems);
  }
  writeLocaleNamespaceLoadersRegistry(baseRoot, localeCodes);
}

function generateForTranslationTree(options) {
  const { baseRoot, baseLocale, logLabel } = options;
  const stems = getStemsForLocaleBase(baseRoot, baseLocale);
  if (stems.length === 0) {
    return 0;
  }
  const locales = getSubdirs(baseRoot);
  let count = 0;
  for (const locale of locales) {
    const localeDir = path.join(baseRoot, locale);
    for (const stem of stems) {
      const data = loadSourceObject(localeDir, stem);
      const outPath = path.join(localeDir, `${stem}.generated.ts`);
      writeGeneratedFile(outPath, data);
      count++;
    }
  }
  emitLoaderModules(baseRoot, stems, locales);
  if (count > 0 && logLabel) {
    console.log(
      `${logLabel}: ${count} file(s) (${stems.length} namespace(s) × ${locales.length} locale(s)).`,
    );
  }
  return count;
}

function main() {
  const coreCount = generateForTranslationTree({
    baseRoot: LOCALES_DIR,
    baseLocale: "en",
    logLabel: "generate-locales (packages/i18n central)",
  });

  let appTotal = 0;
  for (const appName of getAppNames()) {
    const transRoot = path.join(APPS_DIR, appName, "translations");
    if (!fs.existsSync(transRoot)) continue;
    const enDir = path.join(transRoot, "en");
    if (!fs.existsSync(enDir)) continue;
    const n = generateForTranslationTree({
      baseRoot: transRoot,
      baseLocale: "en",
      logLabel: null,
    });
    appTotal += n;
  }
  if (appTotal > 0) {
    console.log(
      `generate-locales (app-store, all apps): ${appTotal} file(s) written under packages/app-store/src/apps/**/translations/.`,
    );
  }

  if (coreCount + appTotal === 0) {
    console.warn("generate-locales-from-yaml: no translation stems found.");
  } else {
    console.log(
      `generate-locales-from-yaml: total ${coreCount + appTotal} .generated.ts file(s) (central: ${coreCount}, apps: ${appTotal}).`,
    );
  }
}

main();
